s = input("give me a string of yours > ")

# [ 'a', 'e', 'o', 'i', 'u']

a = s.count('a')
e = s.count('e')
o = s.count('o')
i = s.count('i')
u = s.count('u')

print("Here're " + str(a + e + o + i + u) + " vowels.")
print("a is " + str(a))
print("e is " + str(e))
print("o is " + str(o))
print("i is " + str(i))
print("u is " + str(u))